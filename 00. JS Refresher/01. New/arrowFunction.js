// Regular Function
function printMyName(name) {
  console.log(name);
}
printMyName("Riam");

// Arrow Function
const printMyName2 = (name2) => {
  console.log(name2);
};
printMyName2("Shazid");

const printNameAndAge = (name3, age) => {
  console.log(name3, age);
};
printMyName2("Shazid", 24);

const multiply = (number) => {
  return number * 3;
};
console.log(multiply(3));
